// the number type
let price: number;
price = 9.95;
console.log(price);

// Decimal Numbers

let counter4: number = 0;
console.log(counter4);

let x: number = 100,
  y: number = 200;

console.log(x, y);

// Binary numbers => following by 0B or 0b

let bin = 0b100;
console.log(bin);

let anotherBin: number = 0b010;
console.log(anotherBin);

// Octal number ==> following by 0o
let octal: number = 0o10;
console.log(octal);

// Hexadecimal number => followed by 0x or 0X
let hexadecimal: number = 0xa;
console.log(hexadecimal);

// big integers

let big: bigint = 9007199254740991n;
console.log(big);
